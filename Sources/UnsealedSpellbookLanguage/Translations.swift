import Foundation

extension AppLanguage {
  var translations: [String: String] {
    switch self {
    case .simplifiedChinese: simplifiedChineseTranslations
    case .traditionalChinese: traditionalChineseTranslations
    case .english: englishTranslations
    }
  }
}

private typealias AchievementTranslation = (title: String, detail: String)

private func makeTranslations(
  ui: [LanguageKey: String],
  achievements: [String: AchievementTranslation]
) -> [String: String] {
  var result = Dictionary(uniqueKeysWithValues: ui.map { ($0.key.rawValue, $0.value) })
  for (id, copy) in achievements {
    result["achievement.\(id).title"] = copy.title
    result["achievement.\(id).detail"] = copy.detail
  }
  return result
}

